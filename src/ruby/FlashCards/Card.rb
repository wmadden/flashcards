module FlashCards

# 
# Represents a flash card.
# 
class Card

  #-----------------------------------------------------------------------------
  #  
  #  Constructor
  #  
  #-----------------------------------------------------------------------------
  
  def initialize( front, back )
    @front = front
    @back = back
  end

  #-----------------------------------------------------------------------------
  #  
  #  Properties
  #  
  #-----------------------------------------------------------------------------
  
  attr_accessor :front, :back, :supplied_answer
  
end

end # END MODULE

